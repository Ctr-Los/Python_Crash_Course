#Dictionaries

#Dictionaries in python allow you to create a list of information that are connected with each other.
#You can store a limitless amout of information.You can even nest dictionaries within dictionaries.


#Here is a simple dictionary:

alien_0 = {'color': 'green', 'points': 5}
print(alien_0['color'])
print(alien_0['points'])

#A dictionary is a collection of "Key-value" pairs. Basically each "key" is connected to a "value".
#A dictionary is wrapped with a {} braces. Inside you put the key-value pairs.
#'color' is the KEY and 'green' is the VALUE
#Each key-value pair is seperated by a comma and you can add as many of them as you like!
